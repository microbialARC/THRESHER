"""Parser for simulation command"""
import argparse
import pandas as pd
import os
# The parser for the overall simulation command
def add_evo_simulator_parser(subparsers):
    """Add the simulation command parser to the subparsers"""
    evo_simulator_parser = subparsers.add_parser(
        "evo_simulator",
        formatter_class=argparse.RawTextHelpFormatter,
        help="Models bacterial evolution through substitution, gene gain/loss, and recombination events."
    )

    evo_simulator_parser.add_argument(
        "--preset",
        required=False,
        type = int,
        help="The preset ID for the simulation. Please refer to the documentation for available preset IDs.\n" \
        "If the preset_id is provided, other parameters will be overwritten.\n" \
        "Currently Not Available."
    )

    evo_simulator_parser.add_argument(
        "--ancestor",
        required=True,
        type=str,
        help="Path to ancestral genome in FASTA format\n. "
        "Generated by THRESHER Genome Profiler as {genome}_concat.fasta."
    )

    evo_simulator_parser.add_argument(
        "--years",
        type=int,
        required=True,
        help="Number of years to simulate evolution."
    )

    evo_simulator_parser.add_argument(
        "--taxa",
        type=int,
        required=True,
        help="Number of taxa(offspring) at the end of the simulation."
    )

    evo_simulator_parser.add_argument(
        "--substitution_model",
        type=str,
        required=True,
        help="Substitution model to use for simulation. Default is GTR.\n"
        "Options are JC69, K2P, K3P, JC, GTR."
    )

    evo_simulator_parser.add_argument(
        "--model_parameters",
        type=str,
        required=True,
        help="""Substitution model parameters (comma-separated, no spaces).

- JC69: No parameters required (leave empty)

- K2P: Single float value
  - Transition/transversion ratio (kappa) > 0

- K3P: Three comma-separated floats (all > 0)
  - Position 1: Transition rate (alpha)
  - Position 2: Transversion rate beta (A↔C, G↔T)
  - Position 3: Transversion rate gamma (A↔T, C↔G)

- GTR: Six comma-separated floats for substitution rates
  - Position 1: A↔G
  - Position 2: A↔C
  - Position 3: A↔T
  - Position 4: G↔C
  - Position 5: G↔T
  - Position 6: C↔T
""" 
    )

    evo_simulator_parser.add_argument(
        "--mutation_rate",
        type=float,
        required=True,
        help="Mutation rate (substitutions per site per year) for simulation"
    )

    # Whether or not to use weighted mutation simulation
    evo_simulator_parser.add_argument(
        "--use_weighted_mutation",
        type=bool,
        required=False,
        default=True,
        help="Whether to use weighted mutation. Default is True.\n"
        "To disable weighted mutation, set to False."
    )

    evo_simulator_parser.add_argument(
        "--weighted_mutation_file",
        type=str,
        required=True,
        help="Path to weighted mutation sites CSV file (position,entropy columns).\n "
        "Generated by THRESHER Genome Profiler as {genome}_entropy.csv.\n "
        "Required when --use_weighted_mutation is True."
    )

    # Whether or not to simulate gene gain/loss
    evo_simulator_parser.add_argument(
        "--use_gain_loss",
        type=bool,
        required=False,
        default=True,
        help="Whether to simulate gene gain/loss events. Default is True.\n"
        "To disable gene gain/loss simulation, set to False."
    )

    evo_simulator_parser.add_argument(
        "--gain_rate",
        type=float,
        required=True,
        help="Gene gain rate (λ) for Poisson process model of gain events.\n"
        "Required when --use_gain_loss is True."
    )

    evo_simulator_parser.add_argument(
        "--loss_rate",
        type=float,
        required=True,
        help="Gene loss rate (λ) for Poisson process model of loss events.\n"
        "Required when --use_gain_loss is True."
    )

    evo_simulator_parser.add_argument(
        "--bin",
        type=str,
        required=True,
        help="Path to genome bin summary CSV file.\n"
        "Generated by THRESHER Genome Profiler as {genome}_bin_summary.csv.\n"
        "Required when --use_gain_loss is True."
    )

    evo_simulator_parser.add_argument(
        "--position_coverage",
        type=str,
        required=True,
        help="Path to position coverage RDS file. \n"
        "Generated by THRESHER Genome Profiler as {genome}_position_coverage.RDS. \n"
        "Required when --use_gain_loss is True."
    )

    evo_simulator_parser.add_argument(
        "--mge_data",
        type=str,
        required=True,
        help="Path to mobile genetic element (MGE) data RDS file. \n"
        "Generated by THRESHER Genome Profiler as {genome}_mges.RDS. \n"
        "Required when --use_gain_loss is True."
    )

    evo_simulator_parser.add_argument(
        "--mge_fasta",
        type=str,
        required=True,
        help="Path to MGE sequence database in FASTA format. \n"
        "Generated by THRESHER Genome Profiler as {genome}_mges_seq.fasta. \n"
        "Required when --use_gain_loss is True."
    )

    evo_simulator_parser.add_argument(
        "--mge_entropy",
        type=str,
        required=True,
        help="Path to directory containing entropy CSV files for all MGEs in the database. \n"
        "Generated by THRESHER Genome Profiler as {genome}_mge_entropy/ directory. \n"
        "Required when --use_gain_loss is True."
    )

    evo_simulator_parser.add_argument(
        "--use_recombination",
        type=bool,
        required=False,
        default=True,
        help="Whether to simulate recombination events. Default is True.\n"
        "To disable recombination simulation, set to False."
    )

    evo_simulator_parser.add_argument(
        "--recombination_rate",
        type=float,
        required=True,
        help="Recombination rate (event per genome per year) for simulation.\n"
        "Required when --use_recombination is True."
    )

    evo_simulator_parser.add_argument(
        "--min_recombination_size",
        type=float,
        required=True,
        help="Minimum recombination size (bp) for simulation. Default is 1 bp. \n"
        "Required when --use_recombination is True."
    )

    evo_simulator_parser.add_argument(
        "--mean_recombination_size",
        type=float,
        required=True,
        help="Mean recombination size (bp) for simulation.\n"
        "Required when --use_recombination is True."
    )

    evo_simulator_parser.add_argument(
        "--nu",
        type=float,
        required=True,
        help="Nu parameter (snps / total length of recombination in bp) for recombination simulation.\n"
        "Required when --use_recombination is True."
    )  

    evo_simulator_parser.add_argument(
        "--output",
        required=False,
        help="Output directory for the simulation results. If not provided, defaults to thresher_evo_simulator_output_<YYYY_MM_DD_HHMMSS> under the current working directory."
    )

    evo_simulator_parser.add_argument(
        "--seed",
        type=int,
        required=False,
        default=int(pd.Timestamp.now().strftime("%Y%m%d")),
        help="""Random seed for reproducibility.
        default is the current date in the format of YYYYMMDD"""
    )

    evo_simulator_parser.add_argument(
        "--prefix",
        default=None,
        help = "Prefix for config file, output files, and analysis naming. If not provided, defaults to timestamp: YYYY_MM_DD_HHMMSS"
    )

    evo_simulator_parser.add_argument(
        "--conda_prefix",
        default=None,
        help = "Directory for conda environments needed for this analysis. If not provided, defaults to <OUTPUT>/conda_envs_<YYYY_MM_DD_HHMMSS>"
    )

    evo_simulator_parser.add_argument(
        "--force",
        action="store_true",
        help="Bypass operating system compatibility checks and force execution of the pipeline.\n"
"This may cause instability or failures."
    )